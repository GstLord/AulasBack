package funcoesaritimeticas;

public class FuncoesAritimeticas {

    public static void main(String[] args){

        int numero =15;
        int numero2 = 25;

        System.out.println(numero+numero2);

        //Operador Módulo(Resto de divisão)

        int restoDivisao = 10 % 3;

        System.out.println(restoDivisao); //vai sempre retorna a sobra de uma divisão

        //classe Math

        double resultado = Math.pow(5.0,3.0); //(base, potencia)

        System.out.println(resultado);

        double resultadoSQRT = Math.sqrt(25); //Square root(Raiz quadrada)

        System.out.println(resultadoSQRT);

    }

}
